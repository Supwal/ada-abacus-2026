DROP TABLE IF EXISTS brasil_cidades CASCADE;
DROP TABLE IF EXISTS brasil_estados CASCADE;

CREATE TABLE brasil_estados (
  id     INTEGER PRIMARY KEY,  -- código IBGE (2 dígitos)
  sigla  VARCHAR(2)   NOT NULL UNIQUE,
  nome   VARCHAR(100) NOT NULL,
  regiao VARCHAR(20)  NOT NULL
);

CREATE TABLE brasil_cidades (
  id        INTEGER PRIMARY KEY,  -- código IBGE (7 dígitos)
  nome      VARCHAR(150) NOT NULL,
  estado_id INTEGER NOT NULL REFERENCES brasil_estados(id),
  capital   BOOLEAN DEFAULT FALSE
);

CREATE INDEX idx_cidades_estado ON brasil_cidades(estado_id);
CREATE INDEX idx_cidades_capital ON brasil_cidades(capital);

