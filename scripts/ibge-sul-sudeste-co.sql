CREATE OR REPLACE VIEW v_cidades_brasil AS
SELECT
  c.id,
  c.nome AS cidade,
  e.sigla,
  e.nome AS estado,
  e.regiao,
  c.capital
FROM brasil_cidades c
JOIN brasil_estados e ON e.id = c.estado_id
ORDER BY e.nome, c.capital DESC, c.nome;
